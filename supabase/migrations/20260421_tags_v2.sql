-- ① restaurants 테이블에 tags_v2 추가
ALTER TABLE restaurants
  ADD COLUMN IF NOT EXISTS tags_v2 JSONB DEFAULT '{}',
  ADD COLUMN IF NOT EXISTS tag_source TEXT DEFAULT 'auto',     -- 'auto' | 'admin' | 'ai'
  ADD COLUMN IF NOT EXISTS tag_confidence FLOAT DEFAULT 0.0,   -- 0~1
  ADD COLUMN IF NOT EXISTS tags_updated_at TIMESTAMPTZ;

-- ② stores 테이블에 tags_v2 추가
ALTER TABLE stores
  ADD COLUMN IF NOT EXISTS tags_v2 JSONB DEFAULT '{}',
  ADD COLUMN IF NOT EXISTS tag_source TEXT DEFAULT 'auto',
  ADD COLUMN IF NOT EXISTS tag_confidence FLOAT DEFAULT 0.0,
  ADD COLUMN IF NOT EXISTS tags_updated_at TIMESTAMPTZ;

-- ③ GIN 인덱스 (JSONB 태그 고속 검색)
CREATE INDEX IF NOT EXISTS idx_restaurants_tags_v2
  ON restaurants USING GIN (tags_v2);

CREATE INDEX IF NOT EXISTS idx_stores_tags_v2
  ON stores USING GIN (tags_v2);

-- ④ 태그 사전 테이블 (관리·검증·가중치용)
CREATE TABLE IF NOT EXISTS tag_dictionary (
  id          SERIAL PRIMARY KEY,
  category    TEXT NOT NULL,            -- '업종', '분위기', '동행' 등
  key         TEXT NOT NULL,            -- 실제 저장 키 (e.g. '삼겹살집')
  label_ko    TEXT NOT NULL,            -- 화면 표시
  emoji       TEXT DEFAULT '',          -- UI 이모지
  weight      FLOAT DEFAULT 1.0,        -- AI 추천 가중치 (높을수록 중요)
  auto_detect TEXT[] DEFAULT '{}',      -- 크롤링 자동감지 키워드 배열
  is_active   BOOLEAN DEFAULT TRUE,
  created_at  TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(category, key)
);

